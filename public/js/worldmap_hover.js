// 수도권
function cap_hover_mover(){
  var a = document.getElementById("capital_hoverimg");
  var b = document.getElementById("capital_flag");

  a.style.display="block";
  b.style.display="block";
}

function cap_hover_mout(){
  var a = document.getElementById("capital_hoverimg");
  var b = document.getElementById("capital_flag");

  a.style.display="none";
  b.style.display="none";
}

